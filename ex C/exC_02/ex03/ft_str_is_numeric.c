int	ft_str_is_numeric(char *str)
{
	int	i;
	int	count;

	i = 0;
	count = 0;
	while (str[i] != '\0')
	{
		if (str[i] >= '0' && str[i] <= '9')
			;
		else
			count++;
		i++;
	}
	if (count > 0)
		return (0);
	return (1);
}
