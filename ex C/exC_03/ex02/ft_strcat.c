char	*ft_strcat(char *dest, char *src)
{
	int	i;
	int	a;

	i = 0;
	a = 0;
	while (dest[i] != '\0')
		i++;
	i--;
	while (dest[i] != '\0' || src[a] != '\0')
	{
		dest[i + 1] = src[a];
		a++;
		i++;
	}
	dest[i + 1] = '\0';
	return (dest);
}
