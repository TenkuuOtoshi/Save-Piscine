int	ft_strcmp(char *s1, char *s2)
{
	int	i;
	int	c;

	i = 0;
	c = 0;
	while (s1[i] == s2[i] && s1[i] != '\0' && s2[i] != '\0')
		i++;
	c = s1[i] - s2[i];
	if (s1[i] < s2[i])
		return (c);
	if (s1[i] > s2[i])
		return (c);
	else if (s1[i] == s2[i])
		return (c);
	return (0);
}
